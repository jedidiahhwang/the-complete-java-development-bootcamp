public class Decimals {
    public static void main(String[] args) {
        double percentage = 73.5;
        System.out.println(percentage + " of percentages are made up");

        double wallet = 20; // Always use double for math calculations. This will be stored as a double.
        int people = 3; // If you use at least one double, Java will return a double.
        System.out.println(wallet /  people);
    }
}
